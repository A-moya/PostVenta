    private void llenarLabel() {
        String cod = txtCod.getText();
        
        controladorCompras control=new controladorCompras();
        modeloInventarios modelo = control.buscarCod(cod);
        
        if (modelo.getId().equals(" ")){
            System.out.println("Es nullo");
        } else {
            txtNomPro.setText(modelo.getDescripcion());
        }
    }


    private void guardarCompra() {
        //GUARDAR EL REGISTRO DE LA COMPRA EN LA TABLA COMPRAS DE LA BD
        //SUMAR AL INVENTARIO LA CANTIDAD AGREGADA
        
        String nomFecha = fechaCompra.getText();
        String cod = txtCod.getText();
        String nom = txtNomPro.getText();
        String cant = txtcantidad.getText();
        
        modeloCompras modelo = new modeloCompras();
        modelo.setId(cod);
        modelo.setFecha(nomFecha);
        modelo.setProducto(nom);
        modelo.setCantidad(cant);
        
        controladorCompras control = new controladorCompras();
        
        if (control.guardarCompra(modelo)) {
            DesktopNotify.showDesktopMessage("Notificacion", "\n Registro Exitoso", DesktopNotify.SUCCESS, 20000);
            notificacion();
            
            //Limpiar los campos de texto
            txtCod.setText("");
            txtNomPro.setText("");
            txtcantidad.setText("");
        } else {
            DesktopNotify.showDesktopMessage("Notificacion", "\n Registro no Exitoso", DesktopNotify.ERROR, 20000);
        }  
    }

    private void cargarFecha() {
        DateTimeFormatter fecha = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        fechaCompra.setText(fecha.format(LocalDateTime.now()));
    }

    private void notificacion() {
        AudioClip sonido;
        sonido = java.applet.Applet.newAudioClip(getClass().getResource("/notificacion/notifi.wav"));
        sonido.play();
    }
