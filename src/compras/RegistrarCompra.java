    private void txtCodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodKeyPressed
        //Buscar producto con letra b
        if (evt.getKeyCode() == KeyEvent.VK_B) {
            InventarioBuscar dialog = new InventarioBuscar(new javax.swing.JFrame(), true, txtCod);
            dialog.setVisible(true);
            if (!txtCod.getText().equals("")) {                
                llenarLabel();
                
                //Posterior a elegir a el producto el cursor se para en este campo
                txtcantidad.requestFocus();
            }
        }

        //Ingresar el código manualmente
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (txtCod.getText().equals("")) {

                System.out.println("Campo vacío");
                JOptionPane.showMessageDialog(null, "Selecciona un producto");              

            } else {
                llenarLabel();
                
                //Posterior a elegir a el código el cursor se va para la casilla de texto cantidad
                txtcantidad.requestFocus();
                System.out.println("Campo lleno");
            }
        }
    }//GEN-LAST:event_txtCodKeyPressed

