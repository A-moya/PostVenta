    //Este método es para realizar el cálculo total - la multiplicación de la cantidad con el valor en cada fila de la tabla
  
    private void jTablaFacturarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTablaFacturarKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int filaeditar = jTablaFacturar.getSelectedRow();
            int numfila = jTablaFacturar.getSelectedColumnCount();
           
           
            if (filaeditar >= 0 && numfila == 1) {
                int cant = Integer.parseInt(jTablaFacturar.getValueAt(filaeditar, 2).toString());
                int valor = Integer.parseInt(jTablaFacturar.getValueAt(filaeditar, 3).toString().replaceAll(patron, ""));
              int total= cant * valor;
                jTablaFacturar.setValueAt("$" + precio_forato.format(Integer.parseInt(String.valueOf(total))), filaeditar, 4);
                sumaTotal();
            }
        }

        //Borrar producto de la tabla
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {

            if (Mtabla.getRowCount() > 0) {

                if (jTablaFacturar.getSelectedRow() != -1) {
                    int filaEliminar = jTablaFacturar.getSelectedRow();
                    Mtabla.removeRow(filaEliminar);
                    sumaTotal();
                }

            } else {
                JOptionPane.showMessageDialog(null, "No ha seleccionado ningun producto");
            }
        }
    }//GEN-LAST:event_jTablaFacturarKeyPressed

