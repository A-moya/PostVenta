    private void llenarTablaCarrito() {
        String dato = txtCod.getText();

        controladorFacturacion control = new controladorFacturacion();
        ArrayList<modeloInventarios> rpta1 = control.listarCarrito(dato);
        ejecutarLLenarTablaCarrito(rpta1);
    }

    //metodo para recorrer los productos agregados al carrito
    public void productos() {
        DateTimeFormatter fecha = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        ArrayList<modeloDetalle> productos = new ArrayList<>();

        //No recuerdo para que es esto-----------
        modeloFacturar modelof = new modeloFacturar();
        modelof.setFecha(fecha.format(LocalDateTime.now()));
        modelof.setIdUsuario("12");
        modelof.setNeto(txtTotal.getText().replaceAll(patron, ""));
        modelof.setImpuesto("0");
        modelof.setTotal(txtTotal.getText().replaceAll(patron, ""));
        modelof.setEstado("4");

        //Recorrernviar la tabla para identificar todo lo que este allí y añadirlo al modelo
        for (int i = 0; i < jTablaFacturar.getRowCount(); i++) {
            modeloDetalle modelo = new modeloDetalle();
            modelo.setIdProducto(jTablaFacturar.getValueAt(i, 0).toString());
            modelo.setCantidad(jTablaFacturar.getValueAt(i, 2).toString());
            modelo.setValor(jTablaFacturar.getValueAt(i, 3).toString().replaceAll(patron, ""));
            modelo.setTotal(jTablaFacturar.getValueAt(i, 4).toString().replaceAll(patron, ""));
            productos.add(modelo);

        }
        controladorFacturacion control = new controladorFacturacion();
        
        
        boolean rpta = control.facturar(modelof, productos);

        if (rpta) {
            this.dispose();
            facturarVentas dialog = new facturarVentas(new javax.swing.JFrame(), true);
            dialog.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Error en guardar los productos");
        }
    }

    //Este método suma la cantidad total de la compra en el label txtTotal
    DecimalFormat precio_forato = new DecimalFormat("###,###"); //Este es para dar formato con punto de mil a los valores 
    String patron = "[^az AZ 0-9]"; //Quita caracteres especiales excepto los que estan allí especificados
    
    int total;

    public void sumaTotal() {
        this.total = 0;
        for (int i = 0; i < jTablaFacturar.getRowCount(); i++) {
            //getValueAt(i, 3) es el metodo que se usa para capturar la informacion de la tabla
            // el cual recibe 2 datos 
            //i = es el número de la fila, es el iterable
            //3 = es el número de la columna que necesitamos para operar, en esta caso es el valor
            //System.out.println("------- "+jTablaFacturar.getValueAt(i, 4).toString().replaceAll(patron, ""));
            int valor = Integer.parseInt(jTablaFacturar.getValueAt(i, 4).toString().replaceAll(patron, ""));
            this.total += valor;
        }
        txtTotal.setText("$" + precio_forato.format(total));
    }

    //
    private void ejecutarLLenarTablaCarrito(ArrayList<modeloInventarios> rpta1) {

        Object[] obj = new Object[5];
        for (modeloInventarios p : rpta1) {
            obj[0] = p.getId();
            obj[1] = p.getDescripcion();
            obj[2] = "1";
            if (p.getValor().equalsIgnoreCase("")) {
                obj[3] = "$0";
            } else {
                obj[3] = "$" + precio_forato.format(Integer.parseInt(p.getValor()));
            }
            obj[4] = "$" + precio_forato.format(1 * Integer.parseInt(p.getValor()));
            Mtabla.addRow(obj);
        }
        confi_tabla();

        //Cada que cargue la tabla, se borra el campo de texto, para esto es esa línea de código
        txtCod.setText("");
        sumaTotal();
    }

