    DecimalFormat precio_forato = new DecimalFormat("###,###"); //Este es para dar formato con punto de mil a los valores
    String patron="[^az AZ 0-9]"; //Quita caracteres especiales excepto los que estan allÃ­ especificados
 
    private void txtEfectivoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEfectivoKeyReleased
        int total = Integer.parseInt(lbTotal.getText().replaceAll(patron, "")) ;
        int efectivo = Integer.parseInt(txtEfectivo.getText().replaceAll(patron, ""));
        
        txtEfectivo.setText("$"+precio_forato.format(efectivo));
        
        
        int gTotal= efectivo-total;
        
        lbCambio.setText("$"+precio_forato.format(gTotal));
        
        if (total > efectivo) {
            lbCambio.setForeground(Color.red);
        } else {
            lbCambio.setForeground(Color.green);
        }
    }//GEN-LAST:event_txtEfectivoKeyReleased

